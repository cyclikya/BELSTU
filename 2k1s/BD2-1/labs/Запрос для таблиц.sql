	create table CLIENTS
	(
		ID_Клиента int primary key,
		Фамилия nvarchar(30),
		Имя nvarchar(30),
		Отчество nvarchar(30),
		Адрес nvarchar(30),
		Телефон int,
		[E-mail] nvarchar(30),
		Признак_скидки bit,
	);
	create table PRODUCTS
	(
		Название_товара nvarchar(30) primary key,
		Количество_на_складе int,
		Цена money,
		Единица_измерения nvarchar(30),
	);
	create table ORDERS
	(
		ID_заказа int primary key,
		Название_товара nvarchar(30) foreign key references PRODUCTS(Название_товара),
		Количество int,
		ID_клиента int foreign key references CLIENTS(ID_клиента),
		Дата_продажи date,
		Цена_продажи MONEY 
	);

